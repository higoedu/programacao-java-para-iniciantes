/*
igual ==
menor <
maior >
menor ou igual <=
maior ou igual >=
diferente !=
*/
public class OperadoresRelacionais {
    public static void main(String[] args) {
        int valor1 = 20;
        int valor2 = 10;

        System.out.println(valor1 == valor2);
        System.out.println(valor1 < valor2);
        System.out.println(valor1 > valor2);
        System.out.println(valor1 <= valor2);
        System.out.println(valor1 >= valor2);
        System.out.println(valor1 != valor2);
    }
}
